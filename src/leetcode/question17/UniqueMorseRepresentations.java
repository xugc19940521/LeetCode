package leetcode.question17;

import java.util.HashMap;
import java.util.Map;

/**
 * @CLassName NniqueMorseRepresentations(804)
 * @Description 国际摩尔斯密码定义一种标准编码方式，将每个字母对应于一个由一系列点和短线组成的字符串， 比如: "a" 对应 ".-", "b" 对应 "-...", "c" 对应 "-.-.", 等等
 *              给定一个单词列表，每个单词可以写成每个字母对应摩尔斯密码的组合。例如，"cab" 可以写成 "-.-.-....-"，(即 "-.-." + "-..." + ".-"字符串的结合)。
 *              我们将这样一个连接过程称作单词翻译。
 *              返回我们可以获得所有词不同单词翻译的数量。
 *              输入：words = ["gin", "zen", "gig", "msg"]
 *              输出：2
 * @Author xugc
 * @Date 2018/9/28 0:11
 * @Version 1.0
 */

public class UniqueMorseRepresentations {

    public static int uniqueMorseRepresentations(String[] words) {

        String[] codes = {".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."};
        Map result = new HashMap();
        for(int i=0;i<words.length;i++){
            StringBuffer stringBuffer = new StringBuffer();

            char[] chars = words[i].toCharArray();
            for(int j=0;j<chars.length;j++){
                stringBuffer.append(codes[chars[j]-97]);
            }
            result.put(stringBuffer.toString(),0);
        }

        return result.size();
    }
}
